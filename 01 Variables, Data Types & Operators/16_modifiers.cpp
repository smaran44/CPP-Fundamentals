#include <iostream>
using namespace std;

int main() {
    unsigned int a = 100;   // Only positive values
    signed int b = -50;     // Both negative and positive
    long int c = 999999999; // Large integer

    cout << "Unsigned Int: " << a << endl;
    cout << "Signed Int: " << b << endl;
    cout << "Long Int: " << c << endl;

    return 0;
}
