#include <iostream>
using namespace std;

int main() {
    int a = 10;         // Integer
    float pi = 3.14;    // Float
    double gravity = 9.81; // Double
    char grade = 'A';   // Character
    bool isPassed = true; // Boolean
    string name = "John"; // String

    cout << "Integer: " << a << endl;
    cout << "Float: " << pi << endl;
    cout << "Double: " << gravity << endl;
    cout << "Character: " << grade << endl;
    cout << "Boolean: " << isPassed << endl;
    cout << "String: " << name << endl;

    return 0;
}
